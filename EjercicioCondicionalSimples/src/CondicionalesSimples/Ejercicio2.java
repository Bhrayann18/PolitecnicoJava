package CondicionalesSimples;

public class Ejercicio2 {

	public static void main(String[] args) {
		
		int numeroUno = 3;
		int numeroDos = 9;
		
		if(numeroUno > numeroDos) {
			
			System.out.println("Potencia " + Math.pow(numeroUno, numeroDos));
		}else {
			System.out.println("Potencia " + Math.pow(numeroDos, numeroUno));
		}

	}

}

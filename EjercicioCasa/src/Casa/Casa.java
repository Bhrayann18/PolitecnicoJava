package Casa;

public class Casa {

	final private String ciudad ="Bogota";
	final private String barrio ="Miravelle";
	final private String color = "Naranja";
	final private int pisos = 3;
	final private int habitaciones = 5;
	final private int baños = 1;
	final private int cocinas = 2;

	public String getCiudad() {
		return ciudad;
	}

	public String getBarrio() {
		return barrio;
	}

	public String getColor() {
		return color;
	}

	public int getPisos() {
		return pisos;
	}

	public int getHabitaciones() {
		return habitaciones;
	}

	public int getBaños() {
		return baños;
	}

	public int getCocinas() {
		return cocinas;
	}

}
